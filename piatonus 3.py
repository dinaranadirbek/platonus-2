# Бағалар 90 алу үшін
агм_бағасы = 90  # Агм орташа бағасы
модуль1_бағасы = 90  # 1-ші модуль орташа бағасы
модуль2_бағасы = 90  # 2-ші модуль орташа бағасы
сессия_бағасы = 90  # Сессия орташа бағасы

# Салмақтар
W_A = 0.3  # Агм
W_1 = 0.3  # 1-ші модуль
W_2 = 0.2  # 2-ші модуль
W_S = 0.2  # Сессия

# Қорытынды баға есептеу
қорытынды_баға = (агм_бағасы * W_A) + (модуль1_бағасы * W_1) + (модуль2_бағасы * W_2) + (сессия_бағасы * W_S)

# Нәтижені шығару
print(f"Қорытынды баға: {қорытынды_баға:.2f}")
